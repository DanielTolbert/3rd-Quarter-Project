public class Point {

    private int x, y, z;
    private double t;

    public Point(int x, int y, int z, double t) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.t = t;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public double getT() {
        return t;
    }
}
